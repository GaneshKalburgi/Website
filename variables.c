//    VARIABILES IN C

#include <stdio.h>
int main()
{

    int a = 56;
    float b = 8.5;
    char c = 'A';
    int d = 10;
    int e = 56 * 6;
    // PRINTING THE VARIABLES IN C
    printf("The value of a  is %d\n", a);
    printf("The value of b  is %f\n", b);
    printf("The value of c  is %c\n\n", c);
    // ALL CALCULATION IS THERE
    printf("sum of a and d is %d\n", a + d);
    printf("sum of a and d is %d\n", a - d);
    printf("sum of a and d is %d\n", a * d);
    printf("sum of a and d is %d\n\n", a / d);
    // MULTIPLICATION --->
    printf("sum of e is %d\n\n", e);
    return 0;
}

/*%d FOR INTEGERS
%f FOR REAL VALUES
%c FOR CHARACTERS*/